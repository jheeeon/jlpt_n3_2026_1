import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const normalizedPath = path.join(rootDir, "data/normalized/vocabulary.n3.json");
const seedPath = path.join(rootDir, "supabase/seed.vocabulary.sql");

function toSqlString(value) {
  if (value === undefined || value === null || value === "") return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function toSqlTextArray(values) {
  if (!values?.length) return "'{}'";
  return `array[${values.map(toSqlString).join(", ")}]`;
}

const vocabulary = JSON.parse(fs.readFileSync(normalizedPath, "utf8"));

const values = vocabulary
  .map((item) => {
    return `(${[
      toSqlString(item.id),
      toSqlString(item.word),
      toSqlString(item.reading),
      toSqlString(item.meaningKo),
      toSqlString(item.meaningEn),
      item.jlptLevel,
      item.priority,
      toSqlTextArray(item.tags),
      toSqlString(item.source),
      item.sourceOrder,
      toSqlString(item.translationStatus),
    ].join(", ")})`;
  })
  .join(",\n");

const sql = `-- Generated by scripts/generate-seed-from-normalized.mjs.
-- Source data: data/normalized/vocabulary.n3.json

insert into vocabulary (
  id,
  word,
  reading,
  meaning_ko,
  meaning_en,
  jlpt_level,
  priority,
  tags,
  source,
  source_order,
  translation_status
) values
${values}
on conflict (word, reading) do update set
  meaning_ko = excluded.meaning_ko,
  meaning_en = excluded.meaning_en,
  priority = excluded.priority,
  tags = excluded.tags,
  source = excluded.source,
  source_order = excluded.source_order,
  translation_status = excluded.translation_status,
  updated_at = now();
`;

fs.writeFileSync(seedPath, sql);

const needsReviewCount = vocabulary.filter(
  (item) => item.translationStatus === "needs_review" || item.tags?.includes("needs_review"),
).length;
console.log(`Generated seed for ${vocabulary.length} vocabulary rows.`);
console.log(`Needs review: ${needsReviewCount}`);
